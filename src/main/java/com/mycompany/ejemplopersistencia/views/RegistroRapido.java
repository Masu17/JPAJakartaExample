    private void BotonRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarseActionPerformed
        try {
            Usuario usuario = new Usuario();
            usuario.setUserName(TextFieldUsuario.getText());
            usuario.setUserPassword(TextFieldContrasegna.getText());
            if (usuario.getUserName().isEmpty() || usuario.getUserPassword().isEmpty()) {
                throw new Exception("No se permiten campos vacios");
            } else {
                new PersistenceMySQl().create(usuario);
                JOptionPane.showMessageDialog(null, "Usuario registrado con exito", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonRegistrarseActionPerformed

